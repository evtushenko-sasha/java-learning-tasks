package com.eval.javaintroduction;

/**
 * The interface Types of data.
 */
public interface TypesOfData {

    /**
     * This method should return byte = 1.
     *
     * @return the byte
     */
    byte getByte();

    /**
     * This method should return boolean = true.
     *
     * @return the boolean
     */
    boolean getBoolean();

    /**
     * This method should return short = 3.
     *
     * @return the short
     */
    short getShort();

    /**
     * This method should return int = 34.
     *
     * @return the int
     */
    int getInt();

    /**
     * This method should return long = 111111111.
     *
     * @return the long
     */
    long getLong();

    /**
     * This method should return double = 8.5123123
     *
     * @return the double
     */
    double getDouble();

    /**
     * This method should return float = 2.7.
     *
     * @return the float
     */
    float getFloat();

    /**
     * This method should return char = 'f'.
     *
     * @return the char
     */
    char getChar();

    /**
     * This method should return String = 'Hello world!'.
     *
     * @return the string
     */
    String getString();

}
